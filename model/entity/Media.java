package model.entity;

public class Media {
    private int id;
    private int tweetId;
    private String url;
    private String type;

    public int getId() {
        return id;
    }

    public int getTweetId() {
        return tweetId;
    }

    public String getUrl() {
        return url;
    }

    public String getType() {
        return type;
    }
}
